//! Lockfile management for reproducible builds (ADR 0070 Phase 1, ADR 0072 Phase 2).
//!
//! **DDD Context:** Build System
//!
//! The lockfile (`beamtalk.lock`) pins exact commit SHAs for git dependencies
//! and exact versions + checksums for native (hex.pm) dependencies.
//! Path dependencies are intentionally NOT locked — they resolve to whatever
//! is on disk, so they are only reproducible within a single checkout.
//!
//! ## Format
//!
//! The lockfile uses a simple, human-readable, git-friendly format:
//!
//! ```text
//! # This file is auto-generated by beamtalk. Do not edit manually.
//! # It pins exact versions of dependencies for reproducible builds.
//!
//! [[package]]
//! name = "json"
//! url = "https://github.com/jamesc/beamtalk-json"
//! reference = "tag:v1.0.0"
//! sha = "abc1234def5678..."
//!
//! [[native_package]]
//! name = "gun"
//! version = "2.1.3"
//! sha = "def456..."
//!
//! [[native_package]]
//! name = "cowboy"
//! version = "2.12.0"
//! sha = "ghi789..."
//! ```

use beamtalk_core::compilation::GitReference;
use camino::Utf8Path;
use miette::{Context, IntoDiagnostic, Result};
use serde::{Deserialize, Serialize};
use std::collections::BTreeMap;
use std::fmt::Write as _;

/// The lockfile filename.
#[allow(dead_code)] // Used by tests; will be called by build system (ADR 0070 Phase 2+)
pub const LOCKFILE_NAME: &str = "beamtalk.lock";

/// The lockfile header comment.
const LOCKFILE_HEADER: &str = "\
# This file is auto-generated by beamtalk. Do not edit manually.
# It pins exact versions of dependencies for reproducible builds.
";

/// A single locked git dependency entry.
#[derive(Debug, Clone, PartialEq, Eq)]
#[allow(dead_code)] // Used by tests; will be called by build system (ADR 0070 Phase 2+)
pub struct LockEntry {
    /// The dependency name.
    pub name: String,
    /// The git repository URL.
    pub url: String,
    /// The requested reference (tag/branch/rev from `beamtalk.toml`).
    pub reference: GitReference,
    /// The exact commit SHA this reference resolved to.
    pub resolved_sha: String,
}

/// A locked native (hex.pm) dependency entry (ADR 0072 Phase 2).
///
/// Stores the exact resolved version and checksum for a hex package,
/// including transitive dependencies resolved by rebar3.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct NativePackageLock {
    /// The hex package name (e.g., "gun", "cowboy", "ranch").
    pub name: String,
    /// The exact resolved version (e.g., "2.1.3").
    pub version: String,
    /// The hex package checksum for integrity verification.
    pub sha: String,
}

/// The full lockfile contents — an ordered collection of lock entries.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct Lockfile {
    /// Locked git packages, keyed by dependency name.
    pub packages: BTreeMap<String, LockEntry>,
    /// Locked native (hex) packages, keyed by package name (ADR 0072 Phase 2).
    pub native_packages: BTreeMap<String, NativePackageLock>,
}

/// Intermediate TOML representation for serialization.
#[derive(Debug, Serialize, Deserialize)]
#[allow(dead_code)] // Constructed by serde deserialization
struct TomlLockfile {
    #[serde(default)]
    package: Vec<TomlLockEntry>,
    #[serde(default)]
    native_package: Vec<TomlNativePackageEntry>,
}

/// A single git package entry in the TOML lockfile.
#[derive(Debug, Serialize, Deserialize)]
#[allow(dead_code)] // Constructed by serde deserialization
struct TomlLockEntry {
    name: String,
    url: String,
    reference: String,
    sha: String,
}

/// A single native (hex) package entry in the TOML lockfile (ADR 0072 Phase 2).
#[derive(Debug, Serialize, Deserialize)]
#[allow(dead_code)] // Constructed by serde deserialization
struct TomlNativePackageEntry {
    name: String,
    version: String,
    sha: String,
}

impl Lockfile {
    /// Create an empty lockfile.
    pub fn new() -> Self {
        Self {
            packages: BTreeMap::new(),
            native_packages: BTreeMap::new(),
        }
    }

    /// Add or update a git lock entry.
    pub fn insert(&mut self, entry: LockEntry) {
        self.packages.insert(entry.name.clone(), entry);
    }

    /// Add or update a native (hex) package lock entry.
    pub fn insert_native(&mut self, entry: NativePackageLock) {
        self.native_packages.insert(entry.name.clone(), entry);
    }

    /// Look up a git lock entry by dependency name.
    pub fn get(&self, name: &str) -> Option<&LockEntry> {
        self.packages.get(name)
    }

    /// Look up a native (hex) package lock entry by name.
    #[allow(dead_code)] // Used by tests; will be called by deps list (ADR 0072 Phase 3+)
    pub fn get_native(&self, name: &str) -> Option<&NativePackageLock> {
        self.native_packages.get(name)
    }

    /// Returns `true` if the lockfile contains no entries of any kind.
    pub fn is_empty(&self) -> bool {
        self.packages.is_empty() && self.native_packages.is_empty()
    }

    /// Returns `true` if the lockfile contains native (hex) package entries.
    pub fn has_native_packages(&self) -> bool {
        !self.native_packages.is_empty()
    }

    /// Remove all native package entries (used before re-populating from rebar3).
    pub fn clear_native_packages(&mut self) {
        self.native_packages.clear();
    }

    /// Read a lockfile from disk.
    ///
    /// Returns `None` if the file does not exist. Returns an error if the
    /// file exists but cannot be parsed.
    pub fn read(project_root: &Utf8Path) -> Result<Option<Self>> {
        let path = project_root.join(LOCKFILE_NAME);
        if !path.exists() {
            return Ok(None);
        }

        let content = std::fs::read_to_string(&path)
            .into_diagnostic()
            .wrap_err_with(|| format!("Failed to read lockfile '{path}'"))?;

        let lockfile =
            Self::parse(&content).wrap_err_with(|| format!("Failed to parse lockfile '{path}'"))?;

        Ok(Some(lockfile))
    }

    /// Parse lockfile content from a string.
    pub fn parse(content: &str) -> Result<Self> {
        let toml_lock: TomlLockfile = toml::from_str(content)
            .into_diagnostic()
            .wrap_err("Invalid lockfile format")?;

        let mut packages = BTreeMap::new();
        for entry in toml_lock.package {
            let reference = parse_reference(&entry.reference).wrap_err_with(|| {
                format!(
                    "Invalid reference '{}' for package '{}'",
                    entry.reference, entry.name
                )
            })?;

            packages.insert(
                entry.name.clone(),
                LockEntry {
                    name: entry.name,
                    url: entry.url,
                    reference,
                    resolved_sha: entry.sha,
                },
            );
        }

        let mut native_packages = BTreeMap::new();
        for entry in toml_lock.native_package {
            native_packages.insert(
                entry.name.clone(),
                NativePackageLock {
                    name: entry.name,
                    version: entry.version,
                    sha: entry.sha,
                },
            );
        }

        Ok(Self {
            packages,
            native_packages,
        })
    }

    /// Write the lockfile to disk.
    ///
    /// If the lockfile has no entries (no git or native deps), the file is
    /// removed to keep the project directory clean.
    pub fn write(&self, project_root: &Utf8Path) -> Result<()> {
        let path = project_root.join(LOCKFILE_NAME);

        if self.is_empty() {
            // Remove stale lockfile if no deps of any kind
            if path.exists() {
                std::fs::remove_file(&path)
                    .into_diagnostic()
                    .wrap_err_with(|| format!("Failed to remove empty lockfile '{path}'"))?;
            }
            return Ok(());
        }

        let content = self.serialize();
        std::fs::write(&path, content)
            .into_diagnostic()
            .wrap_err_with(|| format!("Failed to write lockfile '{path}'"))?;

        Ok(())
    }

    /// Serialize the lockfile to a string.
    pub fn serialize(&self) -> String {
        let mut output = String::new();
        output.push_str(LOCKFILE_HEADER);
        output.push('\n');

        for entry in self.packages.values() {
            output.push_str("[[package]]\n");
            let _ = writeln!(output, "name = \"{}\"", entry.name);
            let _ = writeln!(output, "url = \"{}\"", entry.url);
            let _ = writeln!(
                output,
                "reference = \"{}\"",
                format_reference(&entry.reference)
            );
            let _ = writeln!(output, "sha = \"{}\"", entry.resolved_sha);
            output.push('\n');
        }

        for entry in self.native_packages.values() {
            output.push_str("[[native_package]]\n");
            let _ = writeln!(output, "name = \"{}\"", entry.name);
            let _ = writeln!(output, "version = \"{}\"", entry.version);
            let _ = writeln!(output, "sha = \"{}\"", entry.sha);
            output.push('\n');
        }

        output
    }
}

impl Default for Lockfile {
    fn default() -> Self {
        Self::new()
    }
}

/// Format a `GitReference` for lockfile storage.
///
/// Uses `type:value` format: `tag:v1.0.0`, `branch:main`, `rev:abc1234`.
fn format_reference(reference: &GitReference) -> String {
    match reference {
        GitReference::Tag(tag) => format!("tag:{tag}"),
        GitReference::Branch(branch) => format!("branch:{branch}"),
        GitReference::Rev(rev) => format!("rev:{rev}"),
    }
}

/// Parse a lockfile reference string back to a `GitReference`.
fn parse_reference(s: &str) -> Result<GitReference> {
    if let Some(tag) = s.strip_prefix("tag:") {
        Ok(GitReference::Tag(tag.to_string()))
    } else if let Some(branch) = s.strip_prefix("branch:") {
        Ok(GitReference::Branch(branch.to_string()))
    } else if let Some(rev) = s.strip_prefix("rev:") {
        Ok(GitReference::Rev(rev.to_string()))
    } else {
        miette::bail!(
            "Invalid reference format '{s}' — expected 'tag:...', 'branch:...', or 'rev:...'"
        );
    }
}

/// Parse a rebar3 lockfile (`rebar.lock`) to extract resolved package versions.
///
/// rebar3 writes a lockfile in Erlang term format at `rebar.lock` in its working
/// directory. The format is:
///
/// ```erlang
/// [{<<"package_name">>, {pkg, <<"package_name">>, <<"version">>, ...}, ...},
///  ...].
/// [
///  {pkg_hash, [{<<"package_name">>, <<"HEX_CHECKSUM">>}, ...]},
///  ...
/// ]
/// ```
///
/// We parse this with simple string scanning rather than a full Erlang term
/// parser — the format is stable and well-defined.
pub fn parse_rebar_lock(content: &str) -> Vec<NativePackageLock> {
    let mut packages = Vec::new();

    // Collect version info: {<<"name">>, {pkg, <<"name">>, <<"version">>, ...}, ...}
    let mut versions: BTreeMap<String, String> = BTreeMap::new();
    for cap in extract_pkg_entries(content) {
        versions.insert(cap.0, cap.1);
    }

    // Collect checksums: {<<"name">>, <<"CHECKSUM">>}
    let checksums = extract_pkg_hashes(content);

    for (name, version) in &versions {
        let sha = checksums.get(name).cloned().unwrap_or_default();
        packages.push(NativePackageLock {
            name: name.clone(),
            version: version.clone(),
            sha,
        });
    }

    packages.sort_by(|a, b| a.name.cmp(&b.name));
    packages
}

/// Extract `{<<"name">>, {pkg, <<"name">>, <<"version">>, ...}` entries
/// from the first top-level list in rebar.lock.
fn extract_pkg_entries(content: &str) -> Vec<(String, String)> {
    let mut results = Vec::new();

    // Match pattern: {<<"name">>,{pkg,<<"name">>,<<"version">>,...
    // We look for the pattern with a simple regex-like scan
    let bytes = content.as_bytes();
    let len = bytes.len();
    let mut i = 0;

    while i < len {
        // Look for {<<"
        if i + 3 < len && &bytes[i..i + 4] == b"{<<\"" {
            // Extract the package name
            let name_start = i + 4;
            if let Some(name_end) = content[name_start..].find("\">>,{pkg,<<\"") {
                let name = &content[name_start..name_start + name_end];
                let after_pkg = name_start + name_end + 12; // skip "\">>,{pkg,<<\""

                // Skip the repeated package name
                if let Some(name2_end) = content[after_pkg..].find("\">>,<<\"") {
                    let version_start = after_pkg + name2_end + 7; // skip "\">>,<<\""
                    if let Some(version_end) = content[version_start..].find("\">>") {
                        let version = &content[version_start..version_start + version_end];
                        results.push((name.to_string(), version.to_string()));
                        i = version_start + version_end;
                        continue;
                    }
                }
            }
        }
        i += 1;
    }

    results
}

/// Extract `{<<"name">>, <<"CHECKSUM">>}` entries from the `pkg_hash` section.
///
/// Only reads from `{pkg_hash,[...]}` — not from `{pkg_hash_ext,[...]}`.
fn extract_pkg_hashes(content: &str) -> BTreeMap<String, String> {
    let mut hashes = BTreeMap::new();

    // Find the pkg_hash section (but not pkg_hash_ext)
    // We need to find "pkg_hash," or "pkg_hash,[" specifically
    let Some(hash_section_start) = find_pkg_hash_section(content) else {
        return hashes;
    };

    // Find the closing "]}" to bound the section
    let hash_content = &content[hash_section_start..];
    let section_end = hash_content.find("]}").unwrap_or(hash_content.len());
    let bounded = &hash_content[..section_end];

    // Match pattern: {<<"name">>,<<"CHECKSUM">>}
    let bytes = bounded.as_bytes();
    let len = bytes.len();
    let mut i = 0;

    while i < len {
        if i + 3 < len && &bytes[i..i + 4] == b"{<<\"" {
            let name_start = i + 4;
            if let Some(name_end) = bounded[name_start..].find("\">>,<<\"") {
                let name = &bounded[name_start..name_start + name_end];
                let sha_start = name_start + name_end + 7; // skip "\">>,<<\""
                if let Some(sha_end) = bounded[sha_start..].find("\">>}") {
                    let sha = &bounded[sha_start..sha_start + sha_end];
                    hashes.insert(name.to_string(), sha.to_string());
                    i = sha_start + sha_end;
                    continue;
                }
            }
        }
        i += 1;
    }

    hashes
}

/// Find the start of the `{pkg_hash,[...]}` section, excluding `{pkg_hash_ext,[...]}`.
fn find_pkg_hash_section(content: &str) -> Option<usize> {
    let mut search_from = 0;
    while let Some(pos) = content[search_from..].find("pkg_hash") {
        let abs_pos = search_from + pos;
        let after = &content[abs_pos + 8..]; // skip "pkg_hash"
        // Make sure this is pkg_hash, not pkg_hash_ext
        if !after.starts_with("_ext") {
            return Some(abs_pos);
        }
        search_from = abs_pos + 12; // skip "pkg_hash_ext" to continue
    }
    None
}

/// Read and parse the rebar3 lockfile from the build directory.
///
/// rebar3 writes `rebar.lock` in the directory where it runs
/// (`_build/dev/native/`).
pub fn read_rebar_lock(rebar_base_dir: &Utf8Path) -> Result<Vec<NativePackageLock>> {
    let lock_path = rebar_base_dir.join("rebar.lock");
    if !lock_path.exists() {
        return Ok(Vec::new());
    }

    let content = std::fs::read_to_string(&lock_path)
        .into_diagnostic()
        .wrap_err_with(|| format!("Failed to read rebar.lock at '{lock_path}'"))?;

    Ok(parse_rebar_lock(&content))
}

