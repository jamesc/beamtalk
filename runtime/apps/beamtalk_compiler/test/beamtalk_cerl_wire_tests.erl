build_worker_cerl_variant_compiles_minimum_module_test() ->
    Name = unique_module_name(build_worker),
    Result = beamtalk_build_worker:compile_core_erlang(
        cerl_msg(minimum_module_term(Name))
    ),
    ?assertMatch({ok, _, _}, Result),
    {ok, ModuleName, Binary} = Result,
    ?assertEqual(Name, ModuleName),
    ?assert(is_binary(Binary)),
    ?assert(byte_size(Binary) > 0).

build_worker_cerl_emits_debug_info_test() ->
    %% The build_worker path adds `debug_info' to `compile:forms/2'.
    %% Confirm the chunk is present — ADR 0088 explicitly calls out that
    %% the napkin must exercise both with-and-without-`debug_info' options
    %% so Phase 1 doesn't hit an annotation-chunk surprise on the build
    %% path.
    Name = unique_module_name(debug_info),
    {ok, Name, Binary} = beamtalk_build_worker:compile_core_erlang(
        cerl_msg(minimum_module_term(Name))
    ),
    {ok, {Name, Chunks}} = beam_lib:chunks(Binary, ["Dbgi"]),
    ?assertMatch([{"Dbgi", _}], Chunks).

